// Written in the spirit of reference/theme-reference.md (pop-culture / gaming /
// meme visual riddles, casual friend-group tone). Text-clue "vibe description"
// is the documented fallback for themes that would ideally use an AI-generated
// image, since no image-gen tool is available in this environment (see
// PROGRESS.md). Easiest (100) = most revealing clue, hardest (500) = most
// abstract, matching classic SIGame-style ascending difficulty.

function q(price, clue, accepted, display, imageUrl) {
  // imageUrl is optional -- when present (real screenshot/art fetched and
  // verified during the build), the client shows it alongside the text clue.
  // See PROGRESS.md "Незвичні ігри" section for how these were sourced.
  return {
    price, type: 'text',
    clue: imageUrl ? { kind: 'text', text: clue, imageUrl } : { kind: 'text', text: clue },
    accepted, display: display || accepted[0]
  };
}

function theme(id, name, category, questions) {
  return { id, name, category, questions };
}

